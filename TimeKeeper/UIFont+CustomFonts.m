#import "UIFont+CustomFonts.h"

@implementation UIFont (CustomFonts)

+ (UIFont *)mediumSystemFontOfSize:(CGFloat)size
{
    return [UIFont fontWithName:@"HelveticaNeue-Medium" size:size];
}

@end
